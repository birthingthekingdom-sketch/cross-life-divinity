## Introduction

Biblical prophecy stands as one of the most vital yet misunderstood aspects of God\'s revelation to His people. From Moses\' desire that "all the LORD\'s people were prophets" (Numbers 11:29) to Paul\'s exhortation to "eagerly desire spiritual gifts, especially prophecy" (1 Corinthians 14:1), Scripture consistently elevates the prophetic ministry as essential to God\'s redemptive purposes.

This lesson examines the biblical foundations of prophecy, distinguishing between the Old Testament prophetic office and the New Testament gift of prophecy, while exploring their theological significance and practical applications for contemporary ministry.

## I. The Prophetic Office in the Old Testament

### A. Definition and Function

The Hebrew word for prophet, **נָבִיא** (*nabi*), derives from a root meaning "to call" or "to announce." The prophet served as God\'s authorized spokesman, declaring "Thus says the LORD" with divine authority (Exodus 4:15-16; Jeremiah 1:9).

**Key Characteristics:**
- **Divine calling and commissioning** (Isaiah 6:1-8; Jeremiah 1:4-10)
- **Reception of direct revelation** (Numbers 12:6-8; Deuteronomy 18:18)
- **Covenant enforcement** - calling Israel back to faithfulness (2 Kings 17:13)
- **Foretelling and forthtelling** - both prediction and proclamation

### B. Biblical Examples

**Moses - The Prophetic Standard**
Moses established the pattern for all subsequent prophets. God spoke to him "face to face, as one speaks to a friend" (Exodus 33:11). Deuteronomy 34:10 declares, "Since then, no prophet has risen in Israel like Moses, whom the LORD knew face to face."

**Samuel - Transition to Monarchy**
Samuel bridged the era of judges and kings, demonstrating that "the LORD was with Samuel as he grew up, and he let none of Samuel\'s words fall to the ground" (1 Samuel 3:19).

**Elijah and Elisha - Prophetic Power**
These prophets demonstrated God\'s supremacy through miracles (1 Kings 18; 2 Kings 4-6) while confronting idolatry and injustice.

**The Writing Prophets**
Isaiah, Jeremiah, Ezekiel, and the Twelve spoke God\'s word to specific historical contexts while pointing toward Messianic fulfillment.

### C. The Messianic Prophet

Deuteronomy 18:15-19 prophesied the coming of the ultimate Prophet: "The LORD your God will raise up for you a prophet like me from among you, from your fellow Israelites. You must listen to him."

Jesus fulfilled this prophecy (Acts 3:22-23; 7:37), being the final and complete revelation of God (Hebrews 1:1-2).

## II. The Gift of Prophecy in the New Testament

### A. Pentecost and the Democratization of Prophecy

Joel 2:28-29 promised: "I will pour out my Spirit on all people. Your sons and daughters will prophesy..." Peter declared this fulfilled at Pentecost (Acts 2:16-18), marking a new era where the Spirit enables all believers to prophesy.

### B. The Prophetic Gift in the Church

**Greek Term:** προφητεία (*prophēteia*) - speaking forth God\'s message

**Key Passages:**
- **Romans 12:6** - "If your gift is prophesying, then prophesy in accordance with your faith"
- **1 Corinthians 12:10, 28** - Prophecy listed among spiritual gifts
- **1 Corinthians 14:1-5** - Prophecy builds up, encourages, and comforts the church
- **Ephesians 4:11** - Some given as prophets for equipping the saints

### C. Purpose and Function

According to 1 Corinthians 14:3, prophecy serves three primary functions:
1. **Strengthening** (οἰκοδομή - *oikodomē*) - building up the body
2. **Encouragement** (παράκλησις - *paraklēsis*) - exhortation and comfort  
3. **Comfort** (παραμυθία - *paramythia*) - consolation and reassurance

### D. Distinction from OT Prophetic Office

| Old Testament Office | New Testament Gift |
|---------------------|-------------------|
| Exclusive calling | Available to all believers (1 Cor 14:31) |
| Absolute authority | Subject to testing (1 Thess 5:20-21) |
| Canon-forming | Edifies the church |
| "Thus says the LORD" | "I think I have the Spirit" (1 Cor 7:40) |

## III. Testing and Discerning Prophecy

### A. Biblical Standards

**Deuteronomy 18:20-22** - Fulfillment test for OT prophets  
**1 John 4:1-3** - Test the spirits; does it confess Jesus?  
**1 Corinthians 14:29** - "Let two or three prophets speak, and let the others weigh what is said"  
**1 Thessalonians 5:20-21** - "Do not treat prophecies with contempt but test them all"

### B. Criteria for Evaluation

1. **Theological orthodoxy** - Aligns with Scripture (Galatians 1:8-9)
2. **Christocentric focus** - Points to Jesus (Revelation 19:10)
3. **Moral character** - Good fruit in the prophet\'s life (Matthew 7:15-20)
4. **Edification** - Builds up the church (1 Corinthians 14:3-4)
5. **Witness of the Spirit** - Confirmed by mature believers (1 Corinthians 14:29)

## IV. Theological Synthesis and Application

### Key Theological Principles

1. **Progressive Revelation** - God spoke through prophets in various ways, culminating in Christ (Hebrews 1:1-2)
2. **Covenant Faithfulness** - Prophets called God\'s people back to covenant relationship
3. **Christocentric Focus** - All prophecy ultimately points to and finds fulfillment in Jesus
4. **Spirit Empowerment** - The Holy Spirit enables prophetic ministry in every age

### Practical Application

**For Personal Discipleship:**
- Develop sensitivity to the Holy Spirit\'s voice through Scripture meditation and prayer
- Study how God spoke through prophets to understand His character and ways
- Cultivate humility and teachability in receiving prophetic words

**For Ministry:**
- Create safe environments for prophetic ministry with proper oversight
- Teach congregations to test prophecy biblically rather than reject or accept uncritically
- Model the edifying nature of prophecy in preaching and teaching

**For Spiritual Formation:**
- Recognize that prophecy serves the church, not individual ambition
- Maintain biblical balance between honoring prophetic gifts and testing all things
- Remember that love is greater than any spiritual gift (1 Corinthians 13:1-2)

